        /// </summary>
        Monochrome,
        /// <summary>
        /// Generates only red colors.
        /// </summary>
        Red,
        /// <summary>
        /// Generates only orange colors.
        /// </summary>
        Orange,
        /// <summary>
        /// Generates only yellow colors.
        /// </summary>
        Yellow,
        /// <summary>
        /// Generates only green colors.
        /// </summary>
        Green,
        /// <summary>
        /// Generates only blue colors.
        /// </summary>
        Blue,
        /// <summary>
        /// Generates only purple colors.
        /// </summary>
        Purple,
        /// <summary>
        /// Generates only pink colors.
        /// </summary>
        Pink
    }
}